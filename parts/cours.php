			<h3>Pole débutant</h3>
			<p>Le cours de Pole Dance Débutant est un cours fait de figures très accessibles pour évoluer sans difficultés de manière encadrée. Vous apprendrez également des chorégraphies mêlant danse et figures qui vous feront brûler des calories tout en vous sentant glamour. Osez&nbsp;!</p>


			<h3>Pole inter/avancé</h3>
			<p>Les cours de Pole Dance Inter/ Avancé sont fait pour toutes les élèves pratiquant la discipline depuis plus d’un an.Vous travaillerez de nouvelles figures et des enchaînements afin de toujours progresser.</p>

			<p><strong class="highlight">Ce niveau se décline en deux cours différents :</strong></p>
			<h4>Only Pole</h4>
			<p>Un cours essentiellement axé sur l’apprentissage de nouvelles figures afin de vous dépasser. De belles performances à la clé&nbsp;!</p>

			<h4>Pole and dance</h4>
			<p>Apprentissage de nouvelles figures au sein de chorégraphies entrainantes pour allier sport et glamour. 
			Pour celles qui souhaitent gagner en grâce tout en restant toniques.</p>


			<h3>Pole ados</h3>
			<p>Le cours de Pole Dance du mercredi 17H15 est ouvert à toutes dès 14 ans. Cet entrainement tout niveaux mêle figures de Pole Dance et danse. Un programme complet et adapté aux adolescentes leur permettant de se tonifier et de brûler des calories de manière très fun&nbsp;!</p>
